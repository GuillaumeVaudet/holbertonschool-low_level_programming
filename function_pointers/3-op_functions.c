/**
* op_add - operate an addition
* @a: int
* @b: int
* Return: int - result of the operation
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - operate a substraction
* @a: int
* @b: int
* Return: int - result of the operation
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - operate a multiplication
* @a: int
* @b: int
* Return: int - result of the operation
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - operate a division
* @a: int
* @b: int
* Return: int - result of the operation
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - operate a modulo
* @a: int
* @b: int
* Return: int - result of the operation
*/
int op_mod(int a, int b)
{
	return (a % b);
}
